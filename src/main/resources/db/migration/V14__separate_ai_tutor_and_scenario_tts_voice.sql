INSERT INTO ai_tutor_language_variant (
    ai_tutor_id,
    base_locale,
    display_name,
    created_at,
    updated_at
)
SELECT
    tutor.id,
    'ko',
    '미국 영어 튜터',
    CURRENT_TIMESTAMP,
    CURRENT_TIMESTAMP
FROM ai_tutor tutor
WHERE tutor.accent_locale = 'en-US'
  AND tutor.target_locale = 'en'
  AND tutor.status = 'ACTIVE'
  AND NOT EXISTS (
      SELECT 1
      FROM ai_tutor_language_variant variant
      WHERE variant.ai_tutor_id = tutor.id
        AND variant.base_locale = 'ko'
  );

UPDATE user_profile
SET ai_tutor_id = (
    SELECT tutor.id
    FROM ai_tutor tutor
    WHERE tutor.accent_locale = 'en-US'
      AND tutor.target_locale = 'en'
      AND tutor.status = 'ACTIVE'
)
WHERE ai_tutor_id IS NULL;

ALTER TABLE scenario_language_variant
    ADD COLUMN tts_voice_id BIGINT;

ALTER TABLE scenario_language_variant
    ADD CONSTRAINT fk_scenario_lang_tts_voice_id
        FOREIGN KEY (tts_voice_id) REFERENCES tts_voice (id);

UPDATE scenario_language_variant variant
SET tts_voice_id = (
    SELECT voice.id
    FROM scenario
    JOIN tts_voice voice
      ON voice.provider_voice_id = scenario.tts_voice_set_id
    WHERE scenario.id = variant.scenario_id
)
WHERE EXISTS (
    SELECT 1
    FROM scenario
    JOIN tts_voice voice
      ON voice.provider_voice_id = scenario.tts_voice_set_id
    WHERE scenario.id = variant.scenario_id
);

ALTER TABLE scenario
    DROP COLUMN tts_voice_set_id;

ALTER TABLE ai_tutor
    DROP COLUMN voice_provider;

ALTER TABLE ai_tutor
    DROP COLUMN voice_id;
