    score INTEGER NOT NULL,
    opinion_text TEXT,
    created_at TIMESTAMP(6) NOT NULL,
    CONSTRAINT fk_session_nps_response_learning_session_id FOREIGN KEY (learning_session_id) REFERENCES learning_session (id),
    CONSTRAINT chk_session_nps_response_score CHECK (score BETWEEN 1 AND 5)
);

CREATE TABLE app_version (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    platform VARCHAR(20) NOT NULL,
    version_name VARCHAR(30) NOT NULL,
    build_number BIGINT NOT NULL,
    minimum_supported_build_number BIGINT NOT NULL,
    force_update_reason VARCHAR(500),
    soft_update_reason VARCHAR(500),
    release_note TEXT,
    active BOOLEAN NOT NULL,
    released_at TIMESTAMP(6),
    created_at TIMESTAMP(6) NOT NULL,
    CONSTRAINT uk_app_version_platform_build UNIQUE (platform, build_number),
    CONSTRAINT chk_app_version_build CHECK (build_number >= 0 AND minimum_supported_build_number >= 0)
);

CREATE TABLE writing_expression (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    scenario_id BIGINT NOT NULL,
    expression_type VARCHAR(20) NOT NULL,
    usage_frequency_level VARCHAR(20) NOT NULL,
    target_locale VARCHAR(35) NOT NULL,
    base_locale VARCHAR(35) NOT NULL,
    display_order INTEGER NOT NULL,
    target_expression_text VARCHAR(255) NOT NULL,
    base_expression_meaning_text VARCHAR(255) NOT NULL,
    usage_summary VARCHAR(500) NOT NULL,
    usage_description TEXT NOT NULL,
    representative_question_text VARCHAR(500),
    representative_question_translation VARCHAR(500),
    representative_sentence_text TEXT NOT NULL,
    representative_sentence_translation TEXT NOT NULL,
    representative_image_url VARCHAR(500),
    practice_examples_payload JSONB NOT NULL,
    status VARCHAR(20) NOT NULL,
    created_at TIMESTAMP(6) NOT NULL,
    updated_at TIMESTAMP(6) NOT NULL,
    CONSTRAINT uk_writing_expression_scenario_order UNIQUE (scenario_id, target_locale, base_locale, display_order),
    CONSTRAINT fk_writing_expression_scenario_id FOREIGN KEY (scenario_id) REFERENCES scenario (id),
    CONSTRAINT chk_writing_expression_display_order CHECK (display_order > 0)
);

CREATE TABLE user_writing_expression_completion (
