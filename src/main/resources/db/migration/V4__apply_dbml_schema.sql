CREATE TABLE oauth_identity (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_profile_id BIGINT NOT NULL,
    provider VARCHAR(20) NOT NULL,
    provider_user_id VARCHAR(255) NOT NULL,
    provider_email VARCHAR(255),
    status VARCHAR(20) NOT NULL,
    created_at TIMESTAMP(6) NOT NULL,
    updated_at TIMESTAMP(6) NOT NULL,
    CONSTRAINT fk_oauth_identity_user_profile_id FOREIGN KEY (user_profile_id) REFERENCES user_profile (id)
);

INSERT INTO oauth_identity (
    user_profile_id,
    provider,
    provider_user_id,
    provider_email,
    status,
    created_at,
    updated_at
)
SELECT
    id,
    provider,
    sub,
    email,
    CASE WHEN deleted_at IS NULL THEN 'ACTIVE' ELSE 'UNLINKED' END,
    created_at,
    updated_at
FROM users
WHERE provider IS NOT NULL
  AND sub IS NOT NULL;

CREATE INDEX idx_oauth_identity_provider_user ON oauth_identity (provider, provider_user_id);
CREATE INDEX idx_oauth_identity_user_provider ON oauth_identity (user_profile_id, provider);
